package com.rest.service.inmobile.bean.complient;

public class ComplientResponse {

	public int idComplient;
	public String codeResponse;
	public String messageResponse;
	
	
	public int getIdComplient() {
		return idComplient;
	}
	public void setIdComplient(int idComplient) {
		this.idComplient = idComplient;
	}
	public String getCodeResponse() {
		return codeResponse;
	}
	public void setCodeResponse(String codeResponse) {
		this.codeResponse = codeResponse;
	}
	public String getMessageResponse() {
		return messageResponse;
	}
	public void setMessageResponse(String messageResponse) {
		this.messageResponse = messageResponse;
	}
	
	
	
}
